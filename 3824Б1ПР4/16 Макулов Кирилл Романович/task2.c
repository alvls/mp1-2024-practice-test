int task2(unsigned A[], size_t n) {
    if (n == 0) return 0; // Проверка на пустой массив

    int series_count = 1; // Начинаем с 1, так как первая серия уже существует

    // Проходим по массиву и считаем серии
    for (size_t i = 1; i < n; i++) {
        if (A[i] != A[i - 1]) {
            series_count++; // Увеличиваем счетчик серий при смене элемента
        }
    }

    return series_count; // Возвращаем общее количество серий
}
