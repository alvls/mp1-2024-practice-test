// Задача 3. Дана строка s и натуральное число N.
// Сформировать строку ss длины N следующим образом.
// Если длина строки s больше или равна N,
//   то включить в ss N последних символов строки s.
// Если длина строки s меньше N,
//   то включить в ss все символы строки s, добавив перед ними символы '*'.
#include <string.h>
#include <stdlib.h>

char* task3(char *s, unsigned N)
{
    // Длина исходной строки
    size_t len_s = strlen(s);

    // Выделяем память под новую строку длиной N + 1 (учитывая завершающий ноль-символ)
    char *ss = (char *)malloc(N + 1);
    if (!ss) {
        return NULL; // Не удалось выделить память
    }

    // Если длина строки s больше или равна N, берем последние N символов
    if (len_s >= N) {
        memcpy(ss, s + len_s - N, N);
    } else {
        // Если длина строки s меньше N, заполняем начало новой строки символами '*' до конца строки s
        memset(ss, '*', N - len_s);
        memcpy(ss + N - len_s, s, len_s);
    }

    // Завершаем строку нулевым символом
    ss[N] = '\0';

    return ss;
}
