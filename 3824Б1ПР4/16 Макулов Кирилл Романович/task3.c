#include <stdbool.h>

int task3(char *s) {
    if (s == NULL) return 0; // Проверка на пустую строку

    int word_count = 0;
    bool in_word = false; // Флаг для отслеживания нахождения внутри слова

    while (*s) { // Пока не достигнут конец строки
        if (*s != ' ') { // Если текущий символ не пробел
            if (!in_word) { // Если мы не находимся в слове
                in_word = true; // Начинаем новое слово
                word_count++; // Увеличиваем счетчик слов
            }
        } else {
            in_word = false; // Если пробел, значит мы вышли из слова
        }
        s++; // Переход к следующему символу
    }

    return word_count; // Возвращаем общее количество слов
}
