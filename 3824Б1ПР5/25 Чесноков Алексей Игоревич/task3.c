// Задача 3. Дано целое число N. Не используя функции стандартной библиотеки
// языка С, перевести число в строковое представление в десятичном виде.
char* task3(int N) {
    
    static char buffer[12];
    int i = 0; 
    int is_negative = (N < 0); // Проверяем, отрицательное ли число

    // Если число отрицательное, делаем его положительным для обработки
    if (is_negative) {
        N = -N;
    }

    
    do {
        buffer[i++] = (N % 10) + '0'; // Берем последнюю цифру и конвертируем в символ
        N /= 10; 
    } while (N > 0);

    // Возврат минуса отрицательным числам
    if (is_negative) {
        buffer[i++] = '-';
    }

    
    buffer[i] = '\0';

    // Разворачиваем строку, так как цифры записаны в обратном порядке
    for (int j = 0, k = i - 1; j < k; j++, k--) {
        char temp = buffer[j];
        buffer[j] = buffer[k];
        buffer[k] = temp;
    }

    return buffer; 
}
