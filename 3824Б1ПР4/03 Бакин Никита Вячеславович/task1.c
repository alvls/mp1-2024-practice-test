// Задача 1. Дан непустой массив A, содержащий n натуральных чисел.
// Вычислить сумму S тех элементов массива, значения которых совпадают с их индексами.
#include <stdio.h>

int main() {
    int n;

    printf("Введите размер массива: ");
    scanf("%d", &n);

    if (n <= 0) {
        printf("Размер массива должен быть больше нуля.\n");
        return 1;
    }

    int A[n];

    printf("Введите элементы массива:\n");
    for (int i = 0; i < n; i++) {
        scanf("%d", &A[i]);
    }

    int S = 0;
    for (int i = 0; i < n; i++) {
        if (A[i] == i) {
            S += A[i];
        }
    }

    printf("Сумма элементов, совпадающих с индексами: %d\n", S);

    return 0;
}
