// Задача 3. Даны: символ ch и строка s.
// Сформировать новую строку, из которой удалены все вхождения символа ch.

char* task3(char *s, char ch)
{
    char *write = s;  // Указатель для записи символов
    char *read = s;   // Указатель для чтения символов

    // Проход по строке
    while (*read != '\0') {
        if (*read != ch) {
            *write = *read; // Копируем символ, если он не равен ch
            ++write;
        }
        ++read;
    }

    *write = '\0'; // Завершаем строку
    return s;      // Возвращаем указатель на измененную строку
}
